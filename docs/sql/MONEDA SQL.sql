--LISTAR TODOS LOS REGISTROS POR SUCURSAL
CREATE PROCEDURE SP_L_MONEDA_01
@SUC_ID INT
AS
BEGIN
	SELECT * FROM TM_MONEDA
	WHERE
	SUC_ID = @SUC_ID
	AND EST = 1
END

